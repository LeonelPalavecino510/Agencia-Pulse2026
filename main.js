document.addEventListener('DOMContentLoaded', () => {
    // 1. Language logic
    const langBtns = document.querySelectorAll('.lang-btn');
    const displayLang = document.getElementById('current-lang-display');
    const mobileDisplayLang = document.getElementById('mobile-current-lang');
    
    let currentLang = localStorage.getItem('pulse-lang') || 'es';
    updateLanguage(currentLang);

    langBtns.forEach(btn => {
        btn.addEventListener('click', (e) => {
            e.preventDefault();
            const selectedLang = btn.dataset.lang;
            currentLang = selectedLang;
            localStorage.setItem('pulse-lang', currentLang);
            updateLanguage(currentLang);
        });
    });

    function updateLanguage(lang) {
        document.documentElement.lang = lang;
        if(displayLang) displayLang.textContent = lang.toUpperCase();
        if(mobileDisplayLang) mobileDisplayLang.textContent = lang.toUpperCase();
        
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(el => {
            const key = el.dataset.i18n;
            if (translations[lang][key]) {
                if (translations[lang][key].includes('<')) {
                    el.innerHTML = translations[lang][key];
                } else {
                    el.textContent = translations[lang][key];
                }
            }
        });

        // Update active classes for mobile lang buttons
        document.querySelectorAll('.lang-btn-mobile').forEach(btn => {
            if(btn.dataset.lang === lang) {
                btn.classList.add('text-primary', 'font-bold');
                btn.classList.remove('text-stone-500');
            } else {
                btn.classList.remove('text-primary', 'font-bold');
                btn.classList.add('text-stone-500');
            }
        });
    }

    // 2. Mobile Menu Logic
    const btn = document.getElementById('mobile-menu-btn');
    const menu = document.getElementById('mobile-menu');
    const links = document.querySelectorAll('.mobile-link');

    if (btn && menu) {
        btn.addEventListener('click', () => {
            if (menu.classList.contains('hidden')) {
                menu.classList.remove('hidden');
                menu.classList.add('flex');
            } else {
                menu.classList.add('hidden');
                menu.classList.remove('flex');
            }
        });
    }

    if (links.length > 0 && menu) {
        links.forEach(link => {
            link.addEventListener('click', () => {
                menu.classList.add('hidden');
                menu.classList.remove('flex');
            });
        });
    }
});
